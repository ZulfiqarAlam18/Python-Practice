def cal(num1,op,num2):
    
    if op == '+':
        print(num1+num2)
    
    elif op == '-':
        print(num1-num2)
    elif op == '/':
        print(num1/num2)
    elif op == '*':
        print(num1*num2)
    else:
        print("Invalid Operator")


cal(4,"*",4)



