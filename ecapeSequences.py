# Newline (\n): Moves the cursor to the beginning of the next line.
# Tab (\t): Inserts a tab character.
# Backslash (\\): Inserts a backslash character.
# Single Quote (\'): Inserts a single quote character.
# Double Quote (\"): Inserts a double quote character.

print("Zulfiqar alam is my friend\nHe lives in Bhan SyedAbad")
print("His name is Zulifqar\tAlam")
print("c\\desktop\\pythonpractice\\ecapesequences")
print("Let me call you \'Anna\'")
print("Let me call you \"Anna\"")